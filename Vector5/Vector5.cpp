#include <iostream>
#include <vector>
using namespace std;

template<typename TYPE>
void printVector(const vector<TYPE> v) {
    int SIZE = v.capacity(); 
    for (int i = 0; i < SIZE; i++) {
        cout << v[i] << endl;
    }
}

int main() {
    vector<int> v(10); 
    v[0] = 123;
    v[1] = 456;
    v[2] = -789;

    printVector(v); 

    return 0;
}

